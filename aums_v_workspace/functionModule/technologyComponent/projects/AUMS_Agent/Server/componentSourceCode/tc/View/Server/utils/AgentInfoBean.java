package tc.View.Server.utils;
public class AgentInfoBean {
	private String client_ip;
	private String brno;
	private String zone;
	private String teller_id;
	private String update_mode;
	private String agent_status;
	private String policy_id;
	public String getClient_ip() {
		return client_ip;
	}
	public void setClient_ip(String client_ip) {
		this.client_ip = client_ip;
	}
	public String getBrno() {
		return brno;
	}
	public void setBrno(String brno) {
		this.brno = brno;
	}
	public String getZone() {
		return zone;
	}
	public void setZone(String zone) {
		this.zone = zone;
	}
	public String getTeller_id() {
		return teller_id;
	}
	public void setTeller_id(String teller_id) {
		this.teller_id = teller_id;
	}
	public String getUpdate_mode() {
		return update_mode;
	}
	public void setUpdate_mode(String update_mode) {
		this.update_mode = update_mode;
	}
	public String getAgent_status() {
		return agent_status;
	}
	public void setAgent_status(String agent_status) {
		this.agent_status = agent_status;
	}
	public String getPolicy_id() {
		return policy_id;
	}
	public void setPolicy_id(String policy_id) {
		this.policy_id = policy_id;
	}

}
